public class CompleteSequences {
    public static void main(String[] args) {
        completarSequenciaA();
        completarSequenciaB();
        completarSequenciaC();
        completarSequenciaD();
        completarSequenciaE();
    }

    
    public static void completarSequenciaA() {
        int proximoNumero = 7 + 2;
        System.out.println("Próximo número da sequência a): " + proximoNumero);
    }

 
    public static void completarSequenciaB() {
        int proximoNumero = 64 * 2;
        System.out.println("Próximo número da sequência b): " + proximoNumero);
    }

    
    public static void completarSequenciaC() {
        int proximoNumero = 6 * 6;
        System.out.println("Próximo número da sequência c): " + proximoNumero);
    }

 
    public static void completarSequenciaD() {
        int proximoNumero = 8 * 8;
        System.out.println("Próximo número da sequência d): " + proximoNumero);
    }

  
    public static void completarSequenciaE() {
        int num1 = 5;
        int num2 = 8;
        int proximoNumero = num1 + num2;
        System.out.println("Próximo número da sequência e): " + proximoNumero);
    }
}
